// Assignment 1: Problem 3
// Write a C program to subtract between two numbers.

#include <stdio.h>

int main() {
    int num1, num2, difference;

    // Initialize numbers (or use scanf for user input)
    num1 = 50;
    num2 = 18;

    // Calculate the difference
    difference = num1 - num2;

    // Display the result
    printf("The difference between %d and %d is: %d\n", num1, num2, difference);

    return 0;
}
